; REPRESENTATION OF A 2D MATRIX 
LDR R0,=MATA ; START ADDRESS OF THE MATRIX

MOV R1, #0 ; INDEX I
MOV R2, #0 ; INDEX J
MOV R3, #3 ; #OF ROWS
MOV R4, #3 ; #OF COLUMNS
MOV R5, #4 ; #OF BYTES

MLA R6, R1, R3, R2
MLA R7, R6, R5, R0

LDR R8, [R7]
SWI 0X011











