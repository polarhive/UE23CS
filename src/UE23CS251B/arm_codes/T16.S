; PROGRAM TO FIND SUM OF N NEGATIVE NUMBERS IN AN ARRAY
; USE PREINDEXED WITH WRITEBACK OR AUTOINDEXING ADDRESSING MODE

	LDR R1,=A
	LDR R2,=POSSUM
	MOV R5, #0 ; INTERMEDIARY BUFFER REGISTER
	MOV R6, #6
	SUB R1, R1, #4
	
LOOP:	LDR R3, [R1,#4]!
		MOVS R4, R3, LSL #1
		BCC L1
		ADD R5, R5, R3
L1:		SUBS R6, R6, #1
		BNE LOOP
		
		STR R5,[R2]
     	SWI 0X011

.TEXT 
A: .WORD 10, -20, 30, -40, 50, 60
POSSUM: .WORD 0




 