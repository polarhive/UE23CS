; PROGRAM TO ADD n NUMBERS

		LDR R1,=A
		LDR R5,=SUM
		MOV R4,#5 ; COUNT
		MOV R3,#0 ; INTERMEDIARY RESULT

LOOP:	LDR R2, [R1]
		ADD R3, R3, R2
		ADD R1, R1, #4
		SUB R4, R4, #1

		CMP R4,#0
		BNE LOOP
        
		STR R3,[R5]
		SWI 0X011

.TEXT
A: .WORD 1,2,3,4,5
SUM: .WORD 0

.END