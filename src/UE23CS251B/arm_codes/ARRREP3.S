;FIND THE SUM OF ALL THE ELEMENTS IN A 2D MATRIX 
LDR R0,=MATA ; START ADDRESS OF THE MATRIX
LDR R10,=SUM

MOV R1, #0 ; INDEX I
MOV R2, #0 ; INDEX J
MOV R3, #3 ; #OF ROWS
MOV R4, #3 ; #OF COLUMNS
MOV R5, #4 ; #OF BYTES
MOV R9, #0 ; SUM
L1: MLA R6, R1, R3, R2
	MLA R7, R6, R5, R0

	LDR R8, [R7]
	ADD R9, R9, R8
	ADD R2, R2, #1
	CMP R2, R4
	BNE L1
	MOV R2,#0
	ADD R1, R1, #1
	CMP R1, R3
	BNE L1
	STR R9,[R10]
	
	SWI 0X011











