; PROGRAM TO FIND SUM OF N POSITIVE NUMBERS IN AN ARRAY
; USE PREINDEXED ADDRESSING MODE

	LDR R1,=A
	LDR R2,=POSSUM
	MOV R5, #0 ; INTERMEDIARY BUFFER REGISTER
	MOV R6, #6
	MOV R7, #0
		
LOOP:	LDR R3, [R1,R7]
        ADD R7, R7, #4
		MOVS R4, R3, LSL #1
		BCS L1
		ADD R5, R5, R3
L1:		SUBS R6, R6, #1
		BNE LOOP
		
		STR R5,[R2]
     	SWI 0X011

.TEXT 
A: .WORD 10, -20, 30, -40, 50, 60
POSSUM: .WORD 0




 