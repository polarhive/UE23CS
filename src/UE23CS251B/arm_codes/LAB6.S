; CONSIDER A GIVEN SET OF N ELEMENTS. CHECK WHETHER IT IUS ODD OR EVEN 
; AND STORE IN LOCATIONS AS FOLLOWS:
; LOCEVEN: STORES EVEN NUMBERS
; LOCODD: STORES ODD NUMBERS

	MOV R1, #6  ; COUNT REGISTER
	LDR R2, =A
	LDR R4,=LOCODD
	LDR R5,=LOCEVEN

L1:	LDR R3,[R2]
	TST R3,#1
	BEQ EVEN
	STR R3,[R4]
	ADD R4, R4, #4
	B L2
EVEN: STR R3, [R5]
      ADD R5, R5, #4
  L2: ADD R2, R2, #4
	  SUBS R1, R1, #1
	  BNE L1
	  SWI 0X011





.TEXT
A: .WORD 10, 11, 12, 13, 14, 15
LOCODD: .WORD 0,0,0,0,0,0
LOCEVEN: .WORD 0,0,0,0,0,0

