.DATA
ZERO:  .BYTE 0b11101101
ONE:   .BYTE 0b01100000
TWO:   .BYTE 0b11001110
THREE: .BYTE 0b11101010
FOUR:  .BYTE 0b01100011
FIVE:  .BYTE 0b10101011
SIX:   .BYTE 0b10101111
SEVEN: .BYTE 0b11100000
EIGHT: .BYTE 0b11101111
NINE:  .BYTE 0b11101011
P:     .BYTE 0b11000111
E:     .BYTE 0b10001111
S:     .BYTE 0b10101011
U:     .BYTE 0b01101101


.TEXT
MOV R0, #0
MOV R2, #0

AGAIN:
    SWI 0X202
    CMP R0, #1
    BEQ LOOP1

LOOP1:
    MOV R5, #16
    LDR R1, =ZERO

FORWARD:
    LDRB R0, [R1]
    SWI 0X200
    BL DELAY
    ADD R1, R1, #1
    SUB R5, R5, #1
    CMP R5, #0
    BNE FORWARD

    B LOOP2

LOOP2:
    MOV R5, #16
    LDR R1, =U

BACKWARD:
    LDRB R0, [R1]
    SWI 0X200
    BL DELAY
    SUB R1, R1, #1
    SUB R5, R5, #1
    CMP R5, #0
    BNE BACKWARD

    B AGAIN

DELAY:
    LDR R4, =500000  
LOOP_DELAY:
    SUB R4, R4, #1
    CMP R4, #0
    BNE LOOP_DELAY
    BX LR
